'''Média de 4 notas

Escreva um programa em Python que leia quatro números e calcule a média entre esses números'''

print('Esvreva 4 valores e eu vou dizer qual e a media entre eles.')

n1 = int(input('Escreva o primeiro valor: '))
n2 = int(input('Escreva o segundo valor: '))
n3 = int(input('Escreva o terceiro valor: '))
n4 = int(input('Escreva o quarto valor: '))

media = (n1 + n2 + n3 + n4)/4

print(f'A media entre esse 4 valores e igual a {media}.')